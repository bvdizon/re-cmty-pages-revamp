        <h3 style="margin-bottom: 0;">Demographics</h3>
        <p>In 2016, approximately 1,570 people lived in Chinatown. Today, the population has increased slightly. A large percentage (32%) of seniors between ages 65 and over live here in Chinatown. About 75% of the population comprises Canadian citizens, and the rest are non-Canadian. Only 30% of Chinatown's houses are owner-occupied, and the rest are rented.</p>
    </div>
    <div style="margin-top: 50px;">
        <h3 style="margin-bottom: 0;">Housing</h3>
        <p>Most of Chinatown's real estate developments took place between 1961 and 1990. There was some significant development again between 2011 and 2016. Chinatown is known for its high-rise condos, with many of them boasting Asian architecture. Fewer than 2% of apartments are less than five storeys. Chinatown's apartments are modern, and offer residents fantastic views of the river and the city – for an affordable price. You will also find luxurious apartments worth a couple of millions.</p>
