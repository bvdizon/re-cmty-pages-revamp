        <h3 style="margin-bottom: 0;">Demographics</h3>
        <p>According to the 2016 Census of Canada, 4,170 people lived in Capitol Hill that year. While most of the population falls in the 15-to-65-year bracket, the community has many children (14%) and seniors (9%). Canadian citizens make up almost 90% of the population. There are over 2,000 private houses in Capitol Hill, and their owners occupy around 45% of them. The rest of the houses are rented out. A little over 30% of the homes here were built before 1960.</p>
    </div>
    <div style="margin-top: 50px;">
        <h3 style="margin-bottom: 0;">Housing</h3>
        <p>Capitol Hill has a wide range of houses to suit every need. Almost 45% of the dwellings are semi-detached houses. Single-detached homes are also available in the neighbourhood. There are several apartments, all less than five storeys, and a few row houses.</p>
        <p>Mid-century bungalows for large families and smaller single-family homes perfect for growing families are available here. Some homes come with basement suites. Newer homes in the area are fitted with the latest amenities and modern fixtures. Older homes have either been renovated or torn down to be replaced by semi-detached infills and infills for younger families.</p>
    </div>
    <div style="margin-top: 50px;">
        <h3 style="margin-bottom: 0;">Developer/builder/history</h3>
        <p>Capitol Hill was established as a Calgary neighbourhood in 1948, but the area contains buildings that date back to 1910. Most of the real estate development took place in the 1950s. </p>
