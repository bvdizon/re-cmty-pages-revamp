<h2>Britannia</h2>
<p>A mature well developed residential neighborhood located within the south west quadrant of Calgary, Britannia is surrounded by the Britannia Drive on the north, the Riverdale park on the west, the 50 Avenue S on the south and the Elbow Drive on the east.</p>
<h2>Britannia Real Estate</h2>
<p>Established back in 1956, the community in Britannia is home to some of the most luxurious properties in the entire city of Calgary. Given its strategic location with the city, the Britannia Real Estate offers a quiet and tranquil living experience for its residents living just a stone&rsquo;s throw away from downtown. Homes for sale in Britannia comprise an eclectic mix of housing options from premium single family detached and attached units to upscale apartments and condominiums that are regularly listed an average price of around $2,560,000. This family oriented neighborhood offers its residents a myriad of recreational options ranging from restaurants, shopping complexes, walking trails to the famous Glencoe club and Lindsay Parks Sports Centre and so on.</p>
<h2>Britannia Homes</h2>
<p>Annexed by the City of Calgary in 1910, Britannia&rsquo;s development did not begin until the 1950&rsquo;s. The community takes on a British theme and is named after the Roman name for Britain., with many of the streets in the community named after famous streets in the UK. Located approximately 4km south of downtown, Britannia has views of the Elbow River and the Rocky Mountains, as well as the colossal skyline of the city.</p>
