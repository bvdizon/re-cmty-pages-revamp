                    <h3 style="margin-bottom: 0;">Parks and Recreation &mdash;</h3>
                    <div>
                        <div>
                            <p>Forest Lawn Natural Playground at 10 Avenue and 43rd Street SE is a unique
                                nature-inspired park. It has a basketball court, hills and message boards that the
                                community can post information for other community members on. The park features art
                                projects that tell interesting stories of the people in the community.</p>
                            <p>Elliston Park, located south of 17th/International Avenue and east of 68th Street, is
                                envisioned as an inclusive playground where children and people of all abilities and
                                needs can come together. The park features a variety of equipment including side by side
                                monkey bars, inclusive whirl, conveyor belt, shadow flower, giant star net, expression
                                swing and skyline zip track. There are areas for gathering and shaded seating.</p>
                            <p>Forest Lawn is located close to the Bow River, where you can enjoy boating, kayaking,
                                canoeing and rafting. The water is a bit too cold for swimming but you can make the most
                                of the river through private or guided tours.</p>
                            <p>Like to spend afternoons reading at the library? Forest Lawn Library will nourish your
                                love of reading. The accessible library at 8 Ave St has a children's space, meeting
                                rooms, free Wi-Fi and parking. It is open all week and allows members to return books
                                after business hours.</p>
                            <p>One of the major highlights of living in Forest Lawn or the neighbouring community of
                                Applewood Park is their proximity to Inglewood Bird Sanctuary, a scenic wildlife reserve
                                and a top Calgary attraction. This bird watchers' paradise is home to over 270 species
                                of birds and several species of amphibians, mammals, reptiles and amphibians.</p>
