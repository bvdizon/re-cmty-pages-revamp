<h2>Southview Real Estate Listings Community #732</h2>
<p>Community Established 1950 <br />Southview was the last community to develop of those which comprise the Greater
    Forest Lawn Area. Southview is bounded by 17 Avenue S.E. to the north, 36 Street S.E. to the east, 26 Avenue S.E. to
    the south, and the Bow River to the west. While development of Albert Park/Radisson Heights and Forest Lawn areas
    began in 1910, Southview's history only dates back to 1950. The curvilinear street pattern and street names are
    characteristic of that period. Southview has good transportation access and views of the Downtown, the Bow River
    Valley corridor and the Rocky Mountains.<br /><br /><strong>Southview Schools</strong> <br />Mountain View
