<h2>Montgomery Real Estate Market</h2>
<p>As of 2006, Montgomery was mostly made up of residential attached and detached single-family homes (bungalow, two storey), which makes up 71.8% of the market, with the remaining 28.2% being apartments and condominiums. 37.9% of homes were being used for rental purposes.</p>
