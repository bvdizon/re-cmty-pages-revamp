<h2>Montgomery Homes</h2>
<p>Montgomery is a residential neighbourhood in North West Calgary, bisecting the Tran-Canada Highway. Established in 1911, Montgomery was an autonomous village until being amalgamated by the City of Calgary in 1963, along with Bowness. Situated in a bow shaped portion of the Bow River, Montgomery is a community with a small town atmosphere, with fresh air and green vegetation.</p>
