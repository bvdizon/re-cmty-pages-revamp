<h2>Rutland Park Homes</h2>
<p>Annexed in 1910 as Sarcee Park, Rutland Park was established in 1952. Rutland Park is located North of Mount Royal
    University and west of Crowchild Trail. Rutland Park is named after Rutland county in England and is an ideal place
    for its close proximity to Marda Loop Business Revitalization Zone and Downtown, as well as North Glenmore Park and
    Weaselhead.</p>
