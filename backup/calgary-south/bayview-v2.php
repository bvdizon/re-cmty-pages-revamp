        <h2>Bayview Real Estate History and Overview</h2>
        <p>Located in Calgary's south zone, The Bayview community took on rapid development in 1967. The breathtaking
            Glenmore Reservoir marks the northern border of the community. On the south, the 90th Avenue SW lines the
            boundary. For its one-of-a-kind development, Alcan Design Homes, an Alcan subsidiary, oversaw the
            development and management of Bayview Homes. The neighbourhood&rsquo;s development was rapid, with the first
            homes completed and ready for occupancy by January 1968.</p>
        <p>Housing high-income earners in Calgary, Bayview Homes are classy and unique. As of 2000, its residents had a
            high median household income in the city at $184,383. In 2012, it had a population of 682 occupying 249
            buildings. Thus, Bayview affords the right amount of density that keeps your space exclusive.</p>
        <h3 style="margin-bottom: 0;">Developer History</h3>
        <p>Bayview became a part of the City of Calgary in the 1950s. However, it wasn&rsquo;t until the late 1960s when
            it was developed into a residential neighbourhood. Alcan Design Homes was in charge of its development and
            management. The community developed quickly, with the first set of homes ready for occupancy by early 1968.
        </p>
